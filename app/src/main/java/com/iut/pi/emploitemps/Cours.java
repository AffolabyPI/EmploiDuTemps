public class Cours {
    private String alias;
    private String codeGroupe;
    private String codeRessource;
    private String codeSeance;
    private String dateSeance;
    private String dureeSeance;
    private String heureSeance;
    private String moduleNom;
    private String nomGroupe;
    private String nomProf;

    public Cours() {

    }

    public String getCodeGroupe() {
        return codeGroupe;
    }

    public void setCodeGroupe(String codeGroupe) {
        this.codeGroupe = codeGroupe;
    }

    public String getCodeRessource() {
        return codeRessource;
    }

    public void setCodeRessource(String codeRessource) {
        this.codeRessource = codeRessource;
    }

    public String getCodeSeance() {
        return codeSeance;
    }

    public void setCodeSeance(String codeSeance) {
        this.codeSeance = codeSeance;
    }

    public String getDateSeance() {
        return dateSeance;
    }

    public void setDateSeance(String dateSeance) {
        this.dateSeance = dateSeance;
    }

    public String getDureeSeance() {
        return dureeSeance;
    }

    public void setDureeSeance(String dureeSeance) {
        this.dureeSeance = dureeSeance;
    }

    public String getHeureSeance() {
        return heureSeance;
    }

    public void setHeureSeance(String heureSeance) {
        this.heureSeance = heureSeance;
    }

    public String getModuleNom() {
        return moduleNom;
    }

    public void setModuleNom(String moduleNom) {
        this.moduleNom = moduleNom;
    }

    public String getNomGroupe() {
        return nomGroupe;
    }

    public void setNomGroupe(String nomGroupe) {
        this.nomGroupe = nomGroupe;
    }

    public String getNomProf() {
        return nomProf;
    }

    public void setNomProf(String nomProf) {
        this.nomProf = nomProf;
    }

    public String getAlias() {
        return alias;
    }

    public void setAlias(String alias) {
        this.alias = alias;
    }
}
